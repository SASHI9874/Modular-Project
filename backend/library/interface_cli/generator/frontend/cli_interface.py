#!/usr/bin/env python3
"""
CLI Interface for Agent
Auto-generated by AI Builder Platform
"""

import asyncio
import websockets
import json
import sys
from typing import Optional


class CLIInterface:
    def __init__(self, backend_url: str = "ws://localhost:8000/api/interface-cli/ws/cli"):
        self.backend_url = backend_url
        self.websocket: Optional[websockets.WebSocketClientProtocol] = None
    
    async def connect(self):
        """Connect to backend WebSocket"""
        try:
            self.websocket = await websockets.connect(self.backend_url)
            print(" Connected to agent backend")
            print("Type your message and press Enter. Type 'exit' to quit.\n")
        except Exception as e:
            print(f" Failed to connect to backend: {e}")
            print(f"Make sure backend is running on {self.backend_url}")
            sys.exit(1)
    
    async def send_message(self, message: str):
        """Send message to agent"""
        if not self.websocket:
            print(" Not connected to backend")
            return
        
        data = {
            "type": "user_message",
            "message": message
        }
        
        await self.websocket.send(json.dumps(data))
    
    async def receive_response(self):
        """Receive response from agent"""
        if not self.websocket:
            return None
        
        try:
            response = await self.websocket.recv()
            data = json.loads(response)
            return data.get("content", "")
        except Exception as e:
            print(f" Error receiving response: {e}")
            return None
    
    async def run(self):
        """Main CLI loop"""
        await self.connect()
        
        try:
            while True:
                # Get user input
                try:
                    user_input = input("You: ").strip()
                except EOFError:
                    break
                
                if not user_input:
                    continue
                
                if user_input.lower() in ['exit', 'quit', 'q']:
                    print("👋 Goodbye!")
                    break
                
                # Send to agent
                await self.send_message(user_input)
                
                # Wait for response
                print("Agent: ", end="", flush=True)
                response = await self.receive_response()
                
                if response:
                    print(response)
                else:
                    print("(no response)")
                
                print()  # Blank line for readability
        
        except KeyboardInterrupt:
            print("\n👋 Goodbye!")
        
        finally:
            if self.websocket:
                await self.websocket.close()


def main():
    """Entry point"""
    print("=" * 50)
    print("AI Agent - CLI Interface")
    print("=" * 50)
    print()
    
    cli = CLIInterface()
    asyncio.run(cli.run())


if __name__ == "__main__":
    main()


